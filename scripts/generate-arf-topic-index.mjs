import fs from 'fs/promises';
import path from 'path';
import GithubSlugger from 'github-slugger';

const DEFAULT_DOCS_ROOT = '/home/ivan/dev/eIDAS20/03_arf/docs';
const version = process.env.ARF_TOPIC_VERSION?.trim() || '2.8.0';
const docsRoot = process.env.ARF_DOCS_ROOT?.trim() || DEFAULT_DOCS_ROOT;
const annexSourcePath = path.join(docsRoot, 'annexes', 'annex-2', 'annex-2.02-high-level-requirements-by-topic.md');
const discussionTopicsDir = path.join(docsRoot, 'discussion-topics');
const outputPath = path.join(process.cwd(), 'scripts', 'data', `arf-topics-v${version}.js`);
const annexTopicsBaseUrl = `https://eudi.dev/${version}/annexes/annex-2/annex-2.02-high-level-requirements-by-topic/`;
const discussionTopicsBaseUrl = `https://eudi.dev/${version}/discussion-topics/`;

function stripMarkdownFormatting(value) {
  return value
    .replace(/\[([^\]]+)\]\([^)]+\)/g, '$1')
    .replace(/\[([^\]]+)\]\[[^\]]*\]/g, '$1')
    .replace(/\[([^\]]+)\]/g, '$1')
    .replace(/`([^`]+)`/g, '$1')
    .replace(/\*\*([^*]+)\*\*/g, '$1')
    .replace(/\*([^*]+)\*/g, '$1')
    .replace(/<[^>]+>/g, '')
    .trim();
}

function collectAnnexTopics(source) {
  const slugger = new GithubSlugger();
  const topics = {};
  let inCodeFence = false;

  source.split('\n').forEach((line) => {
    if (/^(```|~~~)/.test(line)) {
      inCodeFence = !inCodeFence;
      return;
    }

    if (inCodeFence) {
      return;
    }

    const match = line.match(/^(#{1,6})\s+(.+)$/);
    if (!match) {
      return;
    }

    const text = stripMarkdownFormatting(match[2]);
    const topicMatch = text.match(/\bTopic\s+(\d+)\b/);
    if (!topicMatch) {
      slugger.slug(text);
      return;
    }

    topics[topicMatch[1]] = {
      headingId: slugger.slug(text),
      text,
    };
  });

  return topics;
}

function extractDiscussionTopicHeading(source) {
  let inCodeFence = false;
  let fallbackHeading = null;

  for (const line of source.split('\n')) {
    if (/^(```|~~~)/.test(line)) {
      inCodeFence = !inCodeFence;
      continue;
    }

    if (inCodeFence) {
      continue;
    }

    const match = line.match(/^#\s+(.+)$/);
    if (match) {
      const headingText = stripMarkdownFormatting(match[1]);
      if (!fallbackHeading) {
        fallbackHeading = headingText;
      }
      if (/\bTopic\s+[A-Z]{1,2}\b/i.test(headingText) || /^[A-Z]{1,2}\s+-\s+/.test(headingText)) {
        return headingText;
      }
    }
  }

  return fallbackHeading;
}

async function collectDiscussionTopics() {
  const entries = await fs.readdir(discussionTopicsDir, { withFileTypes: true });
  const topics = {};

  for (const entry of entries) {
    if (!entry.isFile() || !entry.name.endsWith('.md') || entry.name === 'README.md') {
      continue;
    }

    const stem = entry.name.replace(/\.md$/, '');
    const identifier = stem.split('-', 1)[0]?.toUpperCase() ?? '';
    if (!/^[A-Z]{1,2}$/.test(identifier)) {
      continue;
    }

    const source = await fs.readFile(path.join(discussionTopicsDir, entry.name), 'utf8');
    const headingText = extractDiscussionTopicHeading(source);
    topics[identifier] = {
      path: stem,
      text: headingText ?? `Topic ${identifier}`,
    };
  }

  return topics;
}

async function main() {
  const annexSource = await fs.readFile(annexSourcePath, 'utf8');
  const annexTopics = collectAnnexTopics(annexSource);
  const discussionTopics = await collectDiscussionTopics();

  await fs.mkdir(path.dirname(outputPath), { recursive: true });

  const moduleSource = [
    '// AUTO-GENERATED BY scripts/generate-arf-topic-index.mjs. DO NOT EDIT.',
    '',
    'export const arfTopicReference = {',
    `  version: ${JSON.stringify(version)},`,
    `  annexTopicsBaseUrl: ${JSON.stringify(annexTopicsBaseUrl)},`,
    `  discussionTopicsBaseUrl: ${JSON.stringify(discussionTopicsBaseUrl)},`,
    `  annexTopicCount: ${Object.keys(annexTopics).length},`,
    `  discussionTopicCount: ${Object.keys(discussionTopics).length},`,
    `  annexTopics: ${JSON.stringify(annexTopics, null, 2)},`,
    `  discussionTopics: ${JSON.stringify(discussionTopics, null, 2)},`,
    '};',
    '',
    'export default arfTopicReference;',
    '',
  ].join('\n');

  await fs.writeFile(outputPath, moduleSource);
  console.log(
    `Wrote ${Object.keys(annexTopics).length} annex topics and ${Object.keys(discussionTopics).length} discussion topics to ${outputPath}`,
  );
}

main().catch((error) => {
  console.error(error);
  process.exit(1);
});
