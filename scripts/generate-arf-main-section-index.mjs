import fs from 'fs/promises';
import path from 'path';
import GithubSlugger from 'github-slugger';

const DEFAULT_SOURCE_PATH = '/home/ivan/dev/eIDAS20/03_arf/docs/architecture-and-reference-framework-main.md';
const version = process.env.ARF_MAIN_VERSION?.trim() || '2.8.0';
const sourcePath = process.env.ARF_MAIN_SOURCE?.trim() || DEFAULT_SOURCE_PATH;
const outputPath = path.join(process.cwd(), 'scripts', 'data', `arf-main-v${version}-sections.js`);
const baseUrl = `https://eudi.dev/${version}/architecture-and-reference-framework-main/`;

function stripMarkdownFormatting(value) {
  return value
    .replace(/\[([^\]]+)\]\([^)]+\)/g, '$1')
    .replace(/\[([^\]]+)\]\[[^\]]*\]/g, '$1')
    .replace(/\[([^\]]+)\]/g, '$1')
    .replace(/`([^`]+)`/g, '$1')
    .replace(/\*\*([^*]+)\*\*/g, '$1')
    .replace(/\*([^*]+)\*/g, '$1')
    .replace(/<[^>]+>/g, '')
    .trim();
}

function extractHeadingSectionNumber(value) {
  const match = value.match(/^§?\s*(\d+(?:\.\d+)*)(?:\.)?(?=\s|$)/);
  return match ? match[1] : null;
}

function createHeadingRecords(source) {
  const slugger = new GithubSlugger();
  const headings = [];
  let inCodeFence = false;

  source.split('\n').forEach((line) => {
    if (/^(```|~~~)/.test(line)) {
      inCodeFence = !inCodeFence;
      return;
    }

    if (inCodeFence) {
      return;
    }

    const match = line.match(/^(#{1,6})\s+(.+)$/);
    if (!match) {
      return;
    }

    const text = stripMarkdownFormatting(match[2]);
    const headingId = slugger.slug(text);
    const sectionNumber = extractHeadingSectionNumber(text);

    headings.push({
      depth: match[1].length,
      headingId,
      sectionNumber,
      text,
    });
  });

  return headings;
}

async function main() {
  const source = await fs.readFile(sourcePath, 'utf8');
  const headings = createHeadingRecords(source);
  const sections = Object.fromEntries(
    headings
      .filter((heading) => heading.sectionNumber)
      .map((heading) => [
        heading.sectionNumber,
        {
          headingId: heading.headingId,
          text: heading.text,
        },
      ]),
  );

  await fs.mkdir(path.dirname(outputPath), { recursive: true });

  const moduleSource = [
    '// AUTO-GENERATED BY scripts/generate-arf-main-section-index.mjs. DO NOT EDIT.',
    '',
    'export const arfMainReference = {',
    `  version: ${JSON.stringify(version)},`,
    `  baseUrl: ${JSON.stringify(baseUrl)},`,
    `  sectionCount: ${Object.keys(sections).length},`,
    `  sections: ${JSON.stringify(sections, null, 2)},`,
    '};',
    '',
    'export default arfMainReference;',
    '',
  ].join('\n');

  await fs.writeFile(outputPath, moduleSource);
  console.log(`Wrote ${Object.keys(sections).length} ARF section targets to ${outputPath}`);
}

main().catch((error) => {
  console.error(error);
  process.exit(1);
});
